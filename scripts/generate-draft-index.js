#!/usr/bin/env node
'use strict';

/**
 * generate-draft-index.js
 * Phase 17: Safe indexing and visibility for generated draft pages.
 *
 * Reads Phase 16 draft plans from js/page-drafts.json and outputs
 * js/draft-index.json with deterministic visibility classification.
 *
 * Visibility status values:
 *   hidden_conflict     - existing_page_conflict=true; kept only in this index.
 *   draft_index_only    - valid new draft, tracked but not yet broadly discoverable.
 *   safe_search_visible - strongest new drafts; may be added to wiki-index/sitemap/entity-map.
 *
 * Eligibility for safe_search_visible:
 *   - generated_html_page = true
 *   - confidence = 'high'
 *   - existing_page_conflict = false
 *   - valid title + meta_description in draft plan
 *   - >= 3 related_pages
 *   - >= 3 section_blocks
 *   - max 3 pages total
 *
 * Does NOT modify ranking, search, wiki.js, entity graph scoring, or injection logic.
 * Does NOT force any ranking boost on promoted pages.
 */

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');

// ---------------------------------------------------------------------------
// Constants
// ---------------------------------------------------------------------------

const MAX_SAFE_VISIBLE         = 3;
const MIN_RELATED_PAGES        = 3;
const MIN_SECTION_BLOCKS       = 3;
const PHASE16_ROBOTS_MARKER    = 'content="noindex, follow"';
const PHASE16_DRAFT_CLASS      = 'class="draft-notice"';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readJson(relPath) {
  const abs = path.join(ROOT, relPath);
  return JSON.parse(fs.readFileSync(abs, 'utf8'));
}

function fileExists(relPath) {
  return fs.existsSync(path.join(ROOT, relPath));
}

/**
 * Detect whether a wiki HTML file was generated by Phase 16's page builder.
 * Phase 16 draft pages are identified by their noindex robots directive
 * and the presence of the draft-notice element.
 */
function isPhase16GeneratedPage(slug) {
  const htmlPath = path.join(ROOT, 'wiki', `${slug}.html`);
  if (!fs.existsSync(htmlPath)) return false;
  const html = fs.readFileSync(htmlPath, 'utf8');
  return html.includes(PHASE16_ROBOTS_MARKER) && html.includes(PHASE16_DRAFT_CLASS);
}

// ---------------------------------------------------------------------------
// Load inputs
// ---------------------------------------------------------------------------

const pageDrafts   = readJson('js/page-drafts.json');
const wikiIndex    = readJson('js/wiki-index.json');
const entityMap    = readJson('js/entity-map.json');
const expansionPlan = readJson('js/expansion-plan.json');

const drafts = Array.isArray(pageDrafts.drafts) ? pageDrafts.drafts : [];

// Build wiki-index url set for reference
const wikiIndexUrls = new Set(
  Array.isArray(wikiIndex) ? wikiIndex.map(e => e.url) : []
);

// ---------------------------------------------------------------------------
// First pass: evaluate each draft without applying the max-visible cap
// ---------------------------------------------------------------------------

const evaluated = drafts.map(draft => {
  const slug                  = draft.target_url_slug;
  const htmlExists            = fileExists(`wiki/${slug}.html`);
  const generated_html_page   = htmlExists && isPhase16GeneratedPage(slug);
  const existing_page_conflict = draft.existing_page_conflict === true;
  const confidence            = typeof draft.confidence === 'string' ? draft.confidence : 'unknown';

  const draftData   = draft.draft || {};
  const hasValidTitle    = typeof draftData.title === 'string' && draftData.title.trim().length > 0;
  const hasValidMetaDesc = typeof draftData.meta_description === 'string' && draftData.meta_description.trim().length > 0;
  const relatedCount  = Array.isArray(draft.related_pages)       ? draft.related_pages.length       : 0;
  const sectionCount  = Array.isArray(draftData.section_blocks)  ? draftData.section_blocks.length  : 0;

  const eligible_for_indexing = (
    generated_html_page &&
    confidence === 'high' &&
    !existing_page_conflict &&
    hasValidTitle &&
    hasValidMetaDesc &&
    relatedCount  >= MIN_RELATED_PAGES &&
    sectionCount  >= MIN_SECTION_BLOCKS
  );

  // Collect reasons (always present for transparency)
  const reasons = [];
  if (existing_page_conflict) reasons.push('existing_page_conflict');
  if (!htmlExists)             reasons.push('html_file_missing');
  else if (!generated_html_page) reasons.push('not_phase16_generated_html');
  if (confidence !== 'high')   reasons.push(`confidence_not_high:${confidence}`);
  if (!hasValidTitle)          reasons.push('missing_valid_title');
  if (!hasValidMetaDesc)       reasons.push('missing_valid_meta_description');
  if (relatedCount < MIN_RELATED_PAGES)
    reasons.push(`insufficient_related_pages:${relatedCount}`);
  if (sectionCount < MIN_SECTION_BLOCKS)
    reasons.push(`insufficient_section_blocks:${sectionCount}`);

  return {
    target_path:             draft.target_path,
    title:                   draftData.title || '',
    action_type:             draft.action_type,
    confidence,
    existing_page_conflict,
    generated_html_page,
    eligible_for_indexing,
    visibility_status:       null,   // assigned in second pass
    reasons
  };
});

// ---------------------------------------------------------------------------
// Second pass: assign visibility_status with max-visible cap
// ---------------------------------------------------------------------------

let safeVisibleCount = 0;

for (const entry of evaluated) {
  if (entry.existing_page_conflict) {
    entry.visibility_status = 'hidden_conflict';
  } else if (entry.eligible_for_indexing && safeVisibleCount < MAX_SAFE_VISIBLE) {
    entry.visibility_status = 'safe_search_visible';
    safeVisibleCount++;
  } else {
    entry.visibility_status = 'draft_index_only';
    if (entry.eligible_for_indexing) {
      entry.reasons.push('capped_by_max_safe_visible');
    }
  }
}

// ---------------------------------------------------------------------------
// Build output
// ---------------------------------------------------------------------------

const summary = {
  total_drafts:        evaluated.length,
  hidden_conflict:     evaluated.filter(e => e.visibility_status === 'hidden_conflict').length,
  draft_index_only:    evaluated.filter(e => e.visibility_status === 'draft_index_only').length,
  safe_search_visible: evaluated.filter(e => e.visibility_status === 'safe_search_visible').length,
};

const draftIndex = {
  generated_at: new Date().toISOString(),
  phase: 'phase_17',
  policy: {
    max_safe_search_visible: MAX_SAFE_VISIBLE,
    description: [
      'hidden_conflict: target page already existed at draft-plan time; kept in draft-index.json only.',
      'draft_index_only: valid new draft page tracked but not yet broadly discoverable.',
      'safe_search_visible: strongest new drafts eligible for wiki-index/sitemap/entity-map inclusion.',
    ].join(' | ')
  },
  summary,
  drafts: evaluated
};

const outputPath = path.join(ROOT, 'js', 'draft-index.json');
fs.writeFileSync(outputPath, JSON.stringify(draftIndex, null, 2) + '\n', 'utf8');

console.log(
  `js/draft-index.json written — ` +
  `${summary.total_drafts} draft(s): ` +
  `${summary.hidden_conflict} hidden_conflict, ` +
  `${summary.draft_index_only} draft_index_only, ` +
  `${summary.safe_search_visible} safe_search_visible`
);

// ---------------------------------------------------------------------------
// Controlled asset integration for safe_search_visible pages
// ---------------------------------------------------------------------------

const safeVisible = evaluated.filter(e => e.visibility_status === 'safe_search_visible');

if (safeVisible.length === 0) {
  console.log('No safe_search_visible pages this phase — no asset updates required.');
} else {
  console.log(`${safeVisible.length} safe_search_visible page(s) — regenerate wiki-index, sitemap, entity-map, site-stats as needed:`);
  for (const entry of safeVisible) {
    console.log(`  ${entry.target_path} (${entry.title})`);
  }
}
